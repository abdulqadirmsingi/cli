// Package cmd contains every CLI command DevPulse exposes.
//
// ūüß† Go Lesson #21: The `cmd` package is the top-level boundary between
// user interaction and business logic. Commands live here; actual work
// lives in internal/. This separation makes code easy to test and reason about.
package cmd

import (
	"fmt"
	"os"

	"github.com/charmbracelet/lipgloss"
	"github.com/spf13/cobra"
)

// rootCmd is the base command ‚ÄĒ what runs when you type just `pulse`.
//
// ūüß† Go Lesson #22: Package-level variables are initialized once when the
// program starts. &cobra.Command{...} creates a pointer to a new struct,
// filling in fields by name. Fields not listed get their zero value.
var rootCmd = &cobra.Command{
	Use:   "pulse",
	Short: "track ur grind, no cap ūüĒ•",
	Long:  buildBanner(),
}

// Execute is the public entry point called from main().
//
// ūüß† Go Lesson #23: Exported identifiers start with a CAPITAL letter.
// Execute() is visible to main.go. execute() would be package-private.
// This is Go's entire visibility system ‚ÄĒ no public/private keywords.
func Execute() {
	if err := rootCmd.Execute(); err != nil {
		fmt.Fprintln(os.Stderr, err)
		os.Exit(1)
	}
}

// init() is a special Go function ‚ÄĒ it runs automatically once the package
// is loaded, before main(). Use it only for wiring up, not complex logic.
//
// ūüß† Go Lesson #24: Every package can have multiple init() functions.
// They all run in the order they appear. Here we use them in each cmd/*.go
// file to register subcommands onto rootCmd ‚ÄĒ a clean Cobra pattern.
func init() {
	rootCmd.CompletionOptions.DisableDefaultCmd = true
}

